# Строка октетов, содержащая один бит на каждую виртуальную локальную сеть в
# домене управления на этом магистральном порту. Первый октет
# соответствует VLAN со значениями VlanIndex от 0 до 7;
# второй октет - виртуальным локальным сетям с 8 по 15; и т.д. Старший
# старший бит каждого октета соответствует наименьшему
# значению VlanIndex в этом октете.
#
# Если бит, соответствующий VLAN, установлен в '1', то эта
# VLAN в настоящее время пересылается на этот магистральный порт, т.е,
# она не обрезается; если бит установлен в '0', то эта VLAN
# в настоящее время не пересылается на этот магистральный порт, либо
# потому что она обрезана или по другой причине.
# vlanTrunkPortVlansXmitJoined
# 1.3.6.1.4.1.9.9.46.1.6.1.1.11.5001 = Hex-STRING: 44 30 00 06 A6 00 00 08 02 00 00 20 00 00 00 00
# 1.3.6.1.4.1.9.9.46.1.6.1.1.11.5002 = Hex-STRING: 44 30 00 06 A6 00 00 08 02 00 00 20 00 00 00 00
# 1.3.6.1.4.1.9.9.46.1.6.1.1.11.10101 = ""
# ...
