# Строка октетов, содержащая один бит на VLAN в
# домен управления на этом транковом порту. Первый октет
# соответствует VLAN со значениями VlanIndex от 0 до 7;
# второй октет для VLAN с 8 по 15; и т.д. Наиболее
# значащий бит каждого октета соответствует младшему
# значение VlanIndex в этом октете. Если бит, соответствующий
# для VLAN установлено значение «1», тогда локальная система включена для
# отправка и получение кадров в этой VLAN; если бит установлен
# на «0», то система отключается от отправки и
# получение кадров в этой VLAN.
#
# Чтобы избежать конфликтов между перекрывающимися частичными обновлениями
# несколько менеджеров, т. е. обновления, которые изменяют только часть
# экземпляра этого объекта (например, включить/выключить одиночный
# VLAN на магистральном порту), любая операция SNMP Set,
# экземпляр этого объекта должен также записать значение
# vlanTrunkPortSetSerialNo.
# vlanTrunkPortVlansEnabled
# 1.3.6.1.4.1.9.9.46.1.6.1.1.4.10115 = Hex-STRING: 7F FF FF FF FF FF FF FF FF FF FF FF FF FF FF FF
# 1.3.6.1.4.1.9.9.46.1.6.1.1.4.10116 = Hex-STRING: 00 00 00 00 00 00 00 08 00 00 00 00 00 00 00 00
